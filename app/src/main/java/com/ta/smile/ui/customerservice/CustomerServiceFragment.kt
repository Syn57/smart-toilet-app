package com.ta.smile.ui.customerservice

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import android.widget.TextView
import androidx.fragment.app.Fragment
import androidx.lifecycle.ViewModelProvider
import com.ta.smile.databinding.FragmentCustomerServiceBinding

class CustomerServiceFragment : Fragment() {

    private var _binding: FragmentCustomerServiceBinding? = null

    // This property is only valid between onCreateView and
    // onDestroyView.
    private val binding get() = _binding!!

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {
        val customerServiceViewModel =
            ViewModelProvider(this).get(CustomerServiceViewModel::class.java)

        _binding = FragmentCustomerServiceBinding.inflate(inflater, container, false)
        val root: View = binding.root

        return root
    }

    override fun onDestroyView() {
        super.onDestroyView()
        _binding = null
    }
}